#include <stdio.h>

int main() {
    // Simple C program demonstrating basic features
    printf("Hello, World!\n");
    
    // Variables and arithmetic
    int a = 10;
    int b = 20;
    int sum = a + b;
    
    printf("The sum of %d and %d is %d\n", a, b, sum);
    
    // Simple loop
    printf("Counting from 1 to 5: ");
    for (int i = 1; i <= 5; i++) {
        printf("%d ", i);
    }
    printf("\n");
    
    return 0;
}

